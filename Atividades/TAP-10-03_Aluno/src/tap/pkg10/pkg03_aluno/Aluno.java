package tap.pkg10.pkg03_aluno;

public class Aluno {
    // atributos
    private String nome;
    private String ra;
    
    // Setter
    public void setNome(String n){
        this.nome = n;
    }
    public void setRA(String r){
        this.ra = r;
    }
    
    // Getter
    public String getNome(){
        return nome;
    }
    public String getRA(){
        return ra;
    }
    
}
